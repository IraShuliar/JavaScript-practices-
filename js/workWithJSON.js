function Vector(x, y) {
	this.x = x;
	this.y = y;

	this.plus = function(vector) {
		this.x = this.x + vector.x;
		this.y = this.y + vector.y;
		this.calculateVectorLength();
		return this;
	};
	this.minus = function(vector) {
		this.x = this.x - vector.x;
		this.y = this.y - vector.y;
		this.calculateVectorLength();
		return this;
	};
	
	this.calculateVectorLength = function() {
		var pow = Math.pow(this.x, 2) + Math.pow(this.y, 2);
		var len = Math.sqrt(pow);
		this.length = len;
	};
	this.calculateVectorLength();
}
